// 12MB 
// The entry.S page directory maps the first 4MB of physical memory
// starting at virtual address KERNBASE (that is, it maps virtual
// addresses [KERNBASE, KERNBASE+4MB) to physical addresses [0x80000000, 0x80000000 + 4MB)).
// We choose 4MB because that's how much we can map with one page
// table and it's enough to get us through early boot.  We also map
// virtual addresses [0x80000000, 0x80400000)4MB to physical addresses [0, 4MB); this
// region is critical for a few instructions in entry.S and then we
// never use it again.
//
// Page directories (and page tables), must start on a page boundary,
// hence the "__aligned__" attribute.  Also, because of restrictions
// related to linking and static initializers, we use "x + PTE_P"
// here, rather than the more standard "x | PTE_P".  Everywhere else
// you should use "|" to combine flags.

// identical mapping [0x000080000000 - 0x000080400000] -> [0x000080000000 - 0x000080800000]
// D0:   0x0          
// D1:   0x2         
// D2:   0x0   ----   0x1  ----- 0x2 ----- 0x3
// D3: pgtable
// kernel base mapping [0x000080000000 - 0x000080400000] -> [0xffffffff80000000 - 0xffffffff00400000]
// D0:   0x511          
// D1:   0x510          
// D2:   0x0   ----   0x1  ----- 0x2 ----- 0x3
// D3: pgtable
// 
#define KERN_BASR_MASK  0x0000FFFFFFFFFFFF
#define KERN_BASR_MASK1 0x0000007FFFFFFFFF
#define KERN_BASR_MASK2 0x000000003FFFFFFF

